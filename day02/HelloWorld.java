
public class HelloWorld {

	public static void main(String[] args) {  // void : return 값이 없다는 의미 ( {} 블럭 안에 return 코드 입력X )
											  // static : 실행 전에 메모리에 미리 올려둔다는 의미 ( <-> dynamic : 실행 시 또는 실행 후 그때그때 메모리에 올린다는 의미	)
		System.out.println("Hello World"); // println : 출력 후 줄바꿈
		System.out.println("Hello World");
		System.out.print("Hello World\n");  // \n : 줄바꿈 (문자열 안에서 쓸 수 있음)
		System.out.print("Hello World");
		System.out.print("Hello World"); // print : 출력 (줄바꿈X)

	}

}
